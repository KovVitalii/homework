package homework26.task02;

public class Flat {
  String city;
  String adress;
  int floor;
  int rooms;
  double area;
  int cost;
  String furniture;
  String repair;

  public Flat(String city, String adress, int floor, int rooms, double area, int cost, String furniture, String repair) {
    this.city = city;
    this.adress = adress;
    this.floor = floor;
    this.rooms = rooms;
    this.area = area;
    this.cost = cost;
    this.furniture = furniture;
    this.repair = repair;
  }
}
